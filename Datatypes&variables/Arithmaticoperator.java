public class Arithmaticoperator {
    public static void main(String[] args) {
        System.out.println(5/2);
        System.out.println(5.0/2);
        System.out.println(1/2*10*5);
        System.out.println(5%2);
        System.out.println(3725%2);
        System.out.println(1+'A');
    }
}
